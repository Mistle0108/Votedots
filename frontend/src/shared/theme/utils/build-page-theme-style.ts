    "--card": theme.surfacePrimary,
    "--card-foreground": theme.textPrimary,
    "--popover": theme.surfaceElevated,
    "--popover-foreground": theme.textPrimary,
    "--primary": theme.primaryAction,
    "--primary-foreground": theme.primaryActionText,
    "--secondary": theme.surfaceSecondary,
    "--secondary-foreground": theme.textPrimary,
    "--muted": theme.surfaceSecondary,
