    "--page-theme-surface-primary": theme.surfacePrimary,
    "--page-theme-surface-elevated": theme.surfaceElevated,
    "--page-theme-surface-secondary": theme.surfaceSecondary,
    "--page-theme-border-primary": theme.borderPrimary,
    "--page-theme-border-secondary": theme.borderSecondary,
    "--page-theme-primary-action": theme.primaryAction,
    "--page-theme-primary-action-hover": theme.primaryActionHover,
    "--page-theme-primary-action-text": theme.primaryActionText,
    "--page-theme-accent-warm": theme.accentWarm,
